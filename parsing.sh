#!/bin/bash
if [ "$url" == ""] 
then
  echo "########################################################################"
  echo "|->                          Modo de uso: $0                         <-|"
  echo "|->                   Exemplo: $0 www.site.com.br                    <-|"
  echo "########################################################################"
else
  echo "########################################################################"
  echo "|->                        Buscando hosts...                         <-|"
  echo "########################################################################"

  wget $url
  grep href  index.html | cut -d "/" -f 3 | grep "\." | cut -d '"' -f 1 | grep -v "<l" > lista.txt

  echo "########################################################################"
  echo "|->                      Resolvendo hosts...                         <-|"
  echo "########################################################################"

  for url in $(cat lista.txt);
  do
    host $url | grep "has address";
  done
fi
